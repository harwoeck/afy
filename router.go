func router(w http.ResponseWriter, r *http.Request) {
	if r.URL.Path == "/" {
		http.Redirect(w, r, "/auth/github/login", http.StatusTemporaryRedirect)
		return
	} else if r.URL.Path == "/favicon.png" {
		w.Write(favicon)
		return
	}
	sendError(w, http.StatusNotFound)
}
